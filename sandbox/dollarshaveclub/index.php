			});
		}
		function verifyEmail() {
			var name = $('#name').val();
			var email = $('#email').val();
			var message = $('textarea#message').val();
			//alert('Under contruction for the next 15 mins. Try again then.');
			if (name.length == 0){
				$('#fname').slideDown("slow");
				$('#fmsg').slideUp("slow");
				$('#fmail').slideUp("slow");
			}
			else if (email.length == 0){
				$('#fname').slideUp("slow");
				$('#fmsg').slideUp("slow");
				$('#fmail').slideDown("slow");
			}
			else if (message.length == 0){
				$('#fname').slideUp("slow");
				$('#fmsg').slideDown("slow");
				$('#fmail').slideUp("slow");
			}
			else{
				$('#fname').slideUp("slow");
				$('#fmsg').slideUp("slow");
				$('#fmail').slideUp("slow");
				
				$.ajax({url:"sendcontact.php?name="+name+"&email="+email+"&message="+message,success:function(result){
	    			$('#name').val('');
					$('#email').val('');
					$('textarea#message').val('');
					$('#fsuccess').slideDown("slow");
	  			}});
			}
		}
